#include<stdio.h>
int main()
{
    int length=70,breadth=90,area,perimeter;
    printf("the length of the park is 70 meters.\n");
    printf("the breadth of the park is 90 meters.\n");
    area=length*breadth;
    printf("the area of the park is =%d\n",area);
    perimeter=2*(length+breadth);
    printf("the perimeter of the park is %d\n",perimeter);
    return 0;
}
